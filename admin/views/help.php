  <h2>Админ‑панель: справка</h2>

  <p><b>1.</b> Выберите страницу&nbsp;→ «Открыть».</p>

  <p><b>2.</b> Режимы:</p>
  <ul>
    <li><b>Текст</b> — редактор абзацев.</li>
    <li><b>Картинки</b> — клик по миниатюре.
        <i>Соблюдайте пропорции — они показаны жёлтым баннером.</i></li>
    <li><b>HTML</b> — полный код (доступен после чек‑бокса «Я&nbsp;понимаю»).</li>
    <li><b>Медиабиблиотека</b> — drag‑and‑drop загрузка изображений
        в папку <code>/img</code>.</li>
    <li><b>Комментарии</b> — просмотр всех комментариев; у каждого сообщения
        кнопка «Удалить» мгновенно убирает его с&nbsp;сайта.</li>
    <li><b>Тренеры</b> — управление аккаунтами тренеров:
        <ul style="margin:4px 0 0 16px">
          <li>добавление нового тренера (пароль автоматически хэшируется,
              секретный ключ создаётся и сразу показывается);</li>
          <li>удаление существующего тренера одной кнопкой.</li>
        </ul>
    </li>
  </ul>

  <p>Перед каждым сохранением создаётся резервная копия
     в&nbsp;<code>admin/backup</code>.</p>

  <p>Кнопка <b>⚫ / ☀️</b> переключает тему и&nbsp;запоминает ваш выбор.</p>
